public class CartoonInfo {
    private Integer cartoonId;
    private String cartoonTitle;
    private String cartoonAuthor;
    private String cartoonCategory;
    private String cartoonIntro;
    private String cartoonSerialize;
    private String cartoonCover;
    private String cartoonResource;
    private String cartoonState;

    public Integer getCartoonId() {
        return cartoonId;
    }

    public void setCartoonId(Integer cartoonId) {
        this.cartoonId = cartoonId;
    }

    public String getCartoonTitle() {
        return cartoonTitle;
    }

    public void setCartoonTitle(String cartoonTitle) {
        this.cartoonTitle = cartoonTitle;
    }

    public String getCartoonAuthor() {
        return cartoonAuthor;
    }

    public void setCartoonAuthor(String cartoonAuthor) {
        this.cartoonAuthor = cartoonAuthor;
    }

    public String getCartoonCategory() {
        return cartoonCategory;
    }

    public void setCartoonCategory(String cartoonCategory) {
        this.cartoonCategory = cartoonCategory;
    }

    public String getCartoonIntro() {
        return cartoonIntro;
    }

    public void setCartoonIntro(String cartoonIntro) {
        this.cartoonIntro = cartoonIntro;
    }

    public String getCartoonSerialize() {
        return cartoonSerialize;
    }

    public void setCartoonSerialize(String cartoonSerialize) {
        this.cartoonSerialize = cartoonSerialize;
    }

    public String getCartoonCover() {
        return cartoonCover;
    }

    public void setCartoonCover(String cartoonCover) {
        this.cartoonCover = cartoonCover;
    }

    public String getCartoonResource() {
        return cartoonResource;
    }

    public void setCartoonResource(String cartoonResource) {
        this.cartoonResource = cartoonResource;
    }

    public String getCartoonState() {
        return cartoonState;
    }

    public void setCartoonState(String cartoonState) {
        this.cartoonState = cartoonState;
    }
}